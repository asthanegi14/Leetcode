class Solution {
    String find(Node root, HashMap<String, Integer> map){
        if(root==null){
            return "@";
        }
        
        String str = "";
        if(root.left==null && root.right==null){
            return Integer.toString(root.data);
        }
        
        str+=Integer.toString(root.data);
        str+=find(root.left, map);
        str+=find(root.right, map);
        
        map.put(str, map.getOrDefault(str, 0)+1);
        
        return str;
    }
    int dupSub(Node root) {
        // code here 
        HashMap<String, Integer> map = new HashMap<>();
        find(root, map);
        
        for(int i:map.values()){
            if(i>=2){
                return 1;
            }
        }
        return 0;
    }
}